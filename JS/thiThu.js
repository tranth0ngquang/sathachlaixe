// In câu hỏi ở bên trái
function showQuestionDetails(index, questions) {
  const questionDetails = questions[index];
  const questionsContainer = document.getElementById("questions-container");
  questionsContainer.innerHTML = `
            <div class="question-number">
              <strong>
                Câu ${Number(index + 1)}: ${questionDetails.question}
              </strong>
            </div>

            ${questionDetails.answers.map((answer, i) => `
            <div class="answer p-2"
            style="${isQuizSubmitted && questionDetails.correctAnswer.includes(i + 1) ? "background-color: green; color: white; border-radius: 8px;" : ""};"
            >${i + 1}. ${answer}
            </div>`)
      .join("")}
        `;
}


