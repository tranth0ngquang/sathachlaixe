// Test thử ở clg
function selectAnswer(questionIndex, answerIndex) {
  console.log(
    `Chọn câu trả lời: ${answerIndex + 1} cho câu hỏi: ${questionIndex + 1}`
  );
  showQuestionDetails(questionIndex, getQuestions()); // Update question details with selected answer
}


